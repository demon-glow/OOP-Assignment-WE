    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        try {
            int id = Integer.parseInt(txtDID.getText());

            pst = con.prepareStatement("select* from doctor where id = ?");
            pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();

            if(rs.next()== false ){

                JOptionPane.showMessageDialog(null , "Doctor ID Not Found");
            }
            else {
                String name = rs.getString("dname");
                String speciality = rs.getString("speciality");
                String specialisation = rs.getString("specialisation");
                String tel = rs.getString("tel");

                txtName.setText(name.trim());
                txtSpeciality.setSelectedItem(String.valueOf(speciality).trim());
                txtSpecialisation.setText(String.valueOf(specialisation).trim());
                txtTel.setText(String.valueOf(tel).trim());
            }
        }catch(SQLException ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

