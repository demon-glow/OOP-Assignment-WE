    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
         String dname,speciality,specialisation,tel;
        
        dname = txtName.getText();
        speciality = (String) txtSpeciality.getSelectedItem();
        specialisation = txtSpecialisation.getText();
        tel = txtTel.getText();
        int id = Integer.parseInt(txtDID.getText());
        
        try {
            
            pst = con.prepareStatement ("update  doctor set dname = ? ,speciality = ? ,specialisation = ? , tel = ? where id = ?");
            pst.setString(1,dname);
            pst.setString(2,speciality);
            pst.setString(3,specialisation);
            pst.setString(4,tel);
            pst.setInt(5,id);
            
            pst.executeUpdate();
            
            
            
            JOptionPane.showMessageDialog(null,"Record Updated Sucessfully");
        }catch(SQLException el){
            el.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

