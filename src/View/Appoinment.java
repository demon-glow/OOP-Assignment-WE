    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        // Retrieve data from input fields
    String patientId = txtPatientId.getText();          // Patient ID field
    String patientName = txtPatientName.getText();      // Patient name field
    String contactNo = txtTel.getText();          // Contact number field
    String doctorName = (String) txtName.getSelectedItem(); // Dropdown for doctor names
    String appointmentDate = txtAppointmentDate.getText(); // Appointment date field (e.g., in yyyy-MM-dd format)
    String appointmentTime = txtAppointmentTime.getText(); // Appointment time field
    String description = txtDescription.getText();      // Description field

    try {
        // SQL query to insert appointment details into the database
        String sql = "INSERT INTO appoinment (patientid, patientname, patienttele, dname, adate, atime, description) " +
                     "VALUES (?, ?, ?, ?, ?, ?, ?)";
        pst = con.prepareStatement(sql);

        // Set parameters for the query
        pst.setString(1, patientId);
        pst.setString(2, patientName);
        pst.setString(3, contactNo);
        pst.setString(4, doctorName);
        pst.setString(5, appointmentDate);
        pst.setString(6, appointmentTime);
        pst.setString(7, description);

        // Execute the query
        int rowsAffected = pst.executeUpdate();

        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Appointment Confirmed Successfully!");
        } else {
            JOptionPane.showMessageDialog(null, "Failed to Confirm Appointment.");
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
    } finally {
        // Close resources
        try {
            if (pst != null) pst.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_jButton3ActionPerformed

