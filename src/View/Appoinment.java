    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
          
        // Clear the dropdown before adding new items
    txtName.removeAllItems();
    
    try {
        // Query to get doctor names
        String sql = "SELECT dname FROM doctor";
        pst = con.prepareStatement(sql);
        rs = pst.executeQuery();

        // Iterate through the result set and populate the dropdown
        while (rs.next()) {
            String doctorName = rs.getString("dname");
            txtName.addItem(doctorName); // Assuming txtName is a JComboBox
        }
        
        JOptionPane.showMessageDialog(null, "Doctor list updated successfully.");
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Failed to fetch doctor list.");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

